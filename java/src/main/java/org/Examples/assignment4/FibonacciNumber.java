package org.Examples.assignment4;

public class FibonacciNumber {
    private int value;

    /**
     * Constructor to set the Fibonacci number
     */
    public FibonacciNumber(int value) {
        this.value = value;
    }

    /**
     * Getter for the value
     */
    public int getValue() {
        return value;
    }

    /**
     * Setter for the value (optional if needed for future updates)
     */
    public void setValue(int value) {
        this.value = value;
    }
}
